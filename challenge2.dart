void main() {
  // Menghitung keliling persegi
  int sisi = 10, keliling = 4 * sisi;

  print("Keliling Persegi : $keliling");

  // Menghitung luas lingkaran
  int r = 8;
  double phi = 3.14;
  double luasLingkaran = phi * r * r;

  print(luasLingkaran);

  // Menghitung volume balok
  int p = 4, l = 6, t = 8;
  int volumeBalok = p * l * t;

  print(volumeBalok);
}
